// ============================================================================
// SERVICE WORKER — Workbox-powered PWA (ES Modules)
// Oshocks Junior Bike Shop — Custom with Auto-Clear Cache
// ============================================================================

import { precacheAndRoute, cleanupOutdatedCaches } from 'workbox-precaching';
import { registerRoute } from 'workbox-routing';
import { CacheFirst, NetworkFirst, StaleWhileRevalidate, NetworkOnly } from 'workbox-strategies';
import { ExpirationPlugin } from 'workbox-expiration';
import { CacheableResponsePlugin } from 'workbox-cacheable-response';
import { BackgroundSyncPlugin } from 'workbox-background-sync';
import { setCacheNameDetails, clientsClaim } from 'workbox-core';

// ============================================================================
// CACHE NAMING — Versioned for easy invalidation
// ============================================================================

const CACHE_VERSION = 'v2'; // Bump this to force cache clear on deploy

setCacheNameDetails({
  prefix: 'oshocks',
  suffix: CACHE_VERSION,
  precache: 'precache',
  runtime: 'runtime',
});

// ============================================================================
// PRECACHING — Auto-generated by workbox-webpack-plugin at build time
// The plugin injects the manifest array here via injectManifest
// ============================================================================

precacheAndRoute(self.__WB_MANIFEST || []);

// Clean up old caches from previous versions
cleanupOutdatedCaches();

// ============================================================================
// RUNTIME CACHING — Per-asset-type optimal strategies
// ============================================================================

// Images: Cache First, 30 days, max 200
registerRoute(
  ({ request }) => request.destination === 'image',
  new CacheFirst({
    cacheName: `oshocks-images-${CACHE_VERSION}`,
    plugins: [
      new ExpirationPlugin({
        maxEntries: 200,
        maxAgeSeconds: 30 * 24 * 60 * 60,
      }),
      new CacheableResponsePlugin({
        statuses: [0, 200],
      }),
    ],
  })
);

// Google Fonts: Cache First, 1 year
registerRoute(
  ({ url }) => url.origin === 'https://fonts.googleapis.com' ||
               url.origin === 'https://fonts.gstatic.com',
  new CacheFirst({
    cacheName: `oshocks-fonts-${CACHE_VERSION}`,
    plugins: [
      new ExpirationPlugin({
        maxEntries: 30,
        maxAgeSeconds: 365 * 24 * 60 * 60,
      }),
    ],
  })
);

// API calls: Network First, 1 day
registerRoute(
  ({ url }) => url.pathname.startsWith('/api/'),
  new NetworkFirst({
    cacheName: `oshocks-api-${CACHE_VERSION}`,
    plugins: [
      new ExpirationPlugin({
        maxEntries: 100,
        maxAgeSeconds: 24 * 60 * 60,
      }),
    ],
  })
);

// JS/CSS: Stale While Revalidate, 7 days
registerRoute(
  ({ request }) => request.destination === 'script' ||
                   request.destination === 'style',
  new StaleWhileRevalidate({
    cacheName: `oshocks-static-${CACHE_VERSION}`,
    plugins: [
      new ExpirationPlugin({
        maxEntries: 100,
        maxAgeSeconds: 7 * 24 * 60 * 60,
      }),
    ],
  })
);

// Everything else: Network First fallback
registerRoute(
  ({ request }) => true,
  new NetworkFirst({
    cacheName: `oshocks-fallback-${CACHE_VERSION}`,
    plugins: [
      new ExpirationPlugin({
        maxEntries: 50,
        maxAgeSeconds: 24 * 60 * 60,
      }),
    ],
  })
);

// ============================================================================
// BACKGROUND SYNC — Queue failed cart/order requests
// ============================================================================

registerRoute(
  ({ url }) => url.pathname.startsWith('/api/') &&
               (url.pathname.includes('cart') || url.pathname.includes('order')),
  new NetworkOnly({
    plugins: [
      new BackgroundSyncPlugin('oshocks-cart-queue', {
        maxRetentionTime: 24 * 60,
      }),
    ],
  }),
  'POST'
);

// ============================================================================
// PUSH NOTIFICATIONS
// ============================================================================

self.addEventListener('push', (event) => {
  const data = event.data?.json() || {};

  event.waitUntil(
    self.registration.showNotification(data.title || 'Oshocks', {
      body: data.body || 'New notification',
      icon: data.icon || '/oshocks-pwa.png',
      badge: data.badge || '/apple-touch-icon.png',
      tag: data.tag || 'general',
      data: { url: data.url || '/' },
      actions: [
        { action: 'open', title: 'Open' },
        { action: 'close', title: 'Close' }
      ]
    })
  );
});

self.addEventListener('notificationclick', (event) => {
  event.notification.close();
  const url = event.notification.data?.url || '/';

  if (event.action === 'close') return;

  event.waitUntil(
    clients.matchAll({ type: 'window' }).then(clientList => {
      for (const client of clientList) {
        if (client.url.includes(self.location.origin) && 'focus' in client) {
          return client.focus();
        }
      }
      if (clients.openWindow) return clients.openWindow(url);
    })
  );
});

// ============================================================================
// MESSAGE HANDLING — Skip waiting + CLEAR ALL CACHES
// ============================================================================

self.addEventListener('message', (event) => {
  if (event.data?.type === 'SKIP_WAITING') {
    console.log('🔄 SW: Skip waiting requested');
    self.skipWaiting();
    return;
  }

  if (event.data?.type === 'CLEAR_ALL_CACHES') {
    console.log('🧹 SW: Clear all caches requested');
    event.waitUntil(
      caches.keys().then((cacheNames) => {
        return Promise.all(
          cacheNames.map((cacheName) => {
            console.log(`🗑️ SW: Deleting cache: ${cacheName}`);
            return caches.delete(cacheName);
          })
        );
      }).then(() => {
        console.log('✅ SW: All caches cleared');
        // Notify all clients that caches are cleared
        return self.clients.matchAll({ type: 'window' }).then((clients) => {
          clients.forEach((client) => {
            client.postMessage({ type: 'CACHES_CLEARED' });
          });
        });
      }).catch((err) => {
        console.error('❌ SW: Cache clear failed:', err);
      })
    );
    return;
  }

  if (event.data?.type === 'CHECK_UPDATES') {
    console.log('🔍 SW: Check updates requested');
    event.waitUntil(
      self.registration.update().then(() => {
        console.log('✅ SW: Update check complete');
      }).catch((err) => {
        console.error('❌ SW: Update check failed:', err);
      })
    );
    return;
  }
});

// ============================================================================
// ACTIVATE — Claim clients immediately
// ============================================================================

self.addEventListener('activate', (event) => {
  console.log('✅ SW: Activated, claiming clients...');
  event.waitUntil(clientsClaim());
});

// ============================================================================
// INSTALL — Pre-cache assets
// ============================================================================

self.addEventListener('install', (event) => {
  console.log('📦 SW: Installing...');
  self.skipWaiting();
});

console.log('✅ Oshocks Service Worker loaded — Version:', CACHE_VERSION);
